package Personen;

public class Persoon {
    private String naam;
    private int leeftijd;
    private int lengte;
    private int gewicht;

    public Persoon(String naam, int leeftijd, int lengte, int gewicht) {
        this.naam = naam;
        this.leeftijd = leeftijd;
        this.lengte = lengte;
        this.gewicht = gewicht;
    }
}
